    public String fecha() {
        String fe;
        Calendar fecha = new GregorianCalendar();

        int año = fecha.get(Calendar.YEAR);
        int mes = fecha.get(Calendar.MONTH);
        int dia = fecha.get(Calendar.DAY_OF_MONTH);

        System.out.println("Fecha Actual: "
                + año + "-" + (mes + 1) + "-" + dia);

        fe = año + "-" + (mes + 1) + "-" + dia;
        return fe;
    }

