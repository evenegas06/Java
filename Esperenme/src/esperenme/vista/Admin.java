    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        double tarifa = Double.parseDouble(jLabel32.getText());
        int cantidad = Integer.parseInt(jSpinner3.getValue().toString());
        String servicio = jComboBox3.getSelectedItem().toString() + "_"
        + jComboBox4.getSelectedItem().toString();

        int idServicio = 0;

        if (cantidad <= 0) {
            JOptionPane.showMessageDialog(this, "Seleccione una cantidad de boletos valida.", "Total de boletos: 0", JOptionPane.ERROR_MESSAGE);

        } else {
            double neto = tarifa * cantidad;

            String msj = "Detalles de regalo" + nuevalinea +"Usuario:"+dto2.getNombre() +nuevalinea+"Servicio: " + servicio + nuevalinea
            + "Numero de boletos: " + cantidad + nuevalinea
            + "Costo neto: " + neto + nuevalinea;

            int v = JOptionPane.showConfirmDialog(this, msj, "Confirmar Compra", JOptionPane.YES_NO_CANCEL_OPTION);
            if (v == JOptionPane.YES_OPTION) {
                dtoCompra2 = new CompraDto();
                delCompra2 = new CompraDelegate();

                dtoServicio = new ServicioDto();
                delServicio = new ServicioDelegate();

                ResultSet resultados = delServicio.ejecutarConsulta("SELECT idServicio FROM servicio WHERE nombre= '" + servicio + "'");
                try {
                    while (resultados.next()) {
                        idServicio = Integer.parseInt(resultados.getString(resultados.findColumn("idServicio")));
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(InterfazUsuario.class.getName()).log(Level.SEVERE, null, ex);
                }

                dtoServicio.setIdServicio(idServicio);

                dtoCompra2.setId_Usuario(dto2);
                dtoCompra2.setId_Servicio(dtoServicio);
                dtoCompra2.setCodigo(codigoCompra());
                //   java.util.Date fecha = new Date();
                Calendar fecha = new GregorianCalendar();
                dtoCompra2.setFechaCompra(new java.sql.Date(fecha.getTimeInMillis()));
                dtoCompra2.setCantidad(cantidad);
                dtoCompra2.setCostoNeto(neto);

                try {
                    delCompra2.crearCompra(dtoCompra2);
                } catch (SQLException ex) {
                    Logger.getLogger(InterfazUsuario.class.getName()).log(Level.SEVERE, null, ex);
                }

                JOptionPane.showMessageDialog(this, "Pago realizado con exito.", "Mensaje de Sistema", JOptionPane.INFORMATION_MESSAGE);
                //enviar correo
                CodigoQR codigoQR = new CodigoQR();
                codigoQR.generarQr("Detalles de Compra"+nuevalinea+servicio+" cantidad de boletos: "+cantidad+" Costo total: "+neto+nuevalinea+"Codigo de regalo: "+codigoCompra,"compra.gif");
                String rruta="C:\\Users\\evr_g\\compra.gif";
                enviarCorreo("compra.gif", rruta, "Los detalles del regalo se encuentran en el siguiente archivo.", "Detalles de Regalo", correo);
            }
        }
    }//GEN-LAST:event_jButton11ActionPerformed

