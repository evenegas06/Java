    public String reemplazar(String cadena, String busqueda, String reemplazo) {
        return cadena.replaceFirst(busqueda, reemplazo);
    }

    public String reemplazarTodo(String cadena, String busqueda, String reemplazo) {
        return cadena.replaceAll(busqueda, reemplazo);
    }

    public String buscar() {
        String busqueda = jTextField1.getText();
        String contenido = info.getText();

        Caret seleccion = info.getCaret();
        int inicial = 0;

        if (seleccion.getDot() != seleccion.getMark()) {
            inicial = seleccion.getDot();
        }
        int pos = contenido.indexOf(busqueda, inicial);

        if (pos < 0) {
            JOptionPane.showMessageDialog(this, "No se encontro la palabra solicitada :'(");
            return "";
        }
        int end = pos + busqueda.length();

        info.setCaretPosition(pos);
        info.moveCaretPosition(end);
        return busqueda;
    }

