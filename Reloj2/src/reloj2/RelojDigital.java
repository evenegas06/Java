    private void iniciarReloj() {
        reloj = new Reloj(jLabel4, jLabel3);
        reloj.setEstado(true);
        reloj.start();
        buscaraZonasHorarias();
    }

    private void buscaraZonasHorarias() {
        String[] zonas = TimeZone.getAvailableIDs();
        for (String zona : zonas) {
            jComboBox1.addItem(zona);
        }
        jComboBox1.setSelectedItem(reloj.getZona());
    }

