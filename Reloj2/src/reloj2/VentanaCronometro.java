    private void crearModeloDeTabla(ArrayList<String> tiempos) {

        String columnNames[] = {"Marca", "Tiempo"};
        DefaultTableModel modelo = new DefaultTableModel(columnNames, 0);
        int cont = 1;

        for (String t : tiempos) {
            Object row[] = new Object[2];
            row[0] = cont++;
            row[1] = t;

            modelo.addRow(row);
        }
        jTable1.setModel(modelo);
    }

